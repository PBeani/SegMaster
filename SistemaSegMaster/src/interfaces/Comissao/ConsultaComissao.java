    private void btnSearchMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnSearchMouseClicked
        String s = filter.getText();

        try {
            ComissaoRegrasNegocio regras = new ComissaoRegrasNegocio();
            LinkedList<ComissaoResult> lista;
            if (!"".equals(s)) {
                lista = regras.listaComissaoFiltro(Integer.parseInt(s));
            } else {
                lista = regras.listaComissao();
            }
            montaTabelaComissao(lista);
        } catch (BancoException e) {
            JOptionPane.showMessageDialog(null, "problema no banco de dados");
        } catch (Exception ex) {
            Logger.getLogger(HomeAdmin.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnSearchMouseClicked

    public void montaTabelaComissao(LinkedList<ComissaoResult> lista) {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);

        for (ComissaoResult result : lista) {
            model.addRow(new Object[]{result.getCod(), result.getPedido(), result.getValor()});

        }
        jTable1.setRowSorter(new TableRowSorter(model));
    }

